    private void SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String kembali = dateFormat.format(TglKembali.getDate());
        String pinjam = dateFormat.format(TglPinjam.getDate());
        String no = No.getText();
        String nama = Nama.getText();
        String jenis = Jenis.getSelectedItem().toString();
        if("".equals(No.getText()) || "".equals(Nama.getText()) || "".equals(Alamat.getText()) || 
                "".equals(Jenis.getSelectedItem()) || "".equals(Judul.getText()) || pinjam.equals("") || 
                kembali.equals("") || "".equals(Denda.getText()))
        {
            JOptionPane.showMessageDialog(this, "Harap Lengkapi Data", "Error", JOptionPane.WARNING_MESSAGE);
        } else
        {
            String JS = "";
                if(rbK.isSelected())
                {
                    JS = "K";
                } else
                {
                    JS = "BK";
                }
            String SQL = "INSERT INTO tb_kembali (NoAnggota,NamaPeminjam,AlamatPeminjam,JenisBuku,TanggalPeminjaman,TanggalPengembalian,DendaPelanggaran,Status) "
                    + "VALUES('"+No.getText()+"','"+Nama.getText()+"','"+Alamat.getText()+"','"+Jenis.getSelectedItem()+"','"+pinjam+"','"+kembali+"','"+Denda.getText()+"','"+JS+"')";
            int status = KoneksiDB.execute(SQL);
            if(status == 1)
            {
                JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan!", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                selectData();
            } else
            {
                JOptionPane.showMessageDialog(this, "Data gagal ditambahkan!", "Sukses", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_SaveActionPerformed

