    private void selectData()
    {
        String kolom[] = {"NoAnggota","NamaPeminjam","AlamatPeminjam","JenisBuku","JudulBuku","TanggalPeminjaman","TanggalPengembalian","DendaPelanggaran","Status"};
        DefaultTableModel dtm = new DefaultTableModel(null, kolom);
        String SQL = "SELECT * FROM tb_kembali";
        ResultSet rs = KoneksiDB.executeQuery(SQL);
        try
        {
            while (rs.next())
            {
                String NoAnggota = rs.getString(1);
                String NamaPeminjam = rs.getNString(2);
                String AlamatPeminjam = rs.getString(3);
                String JenisBuku = rs.getString(4);
                String JudulBuku = rs.getString(5);
                String TanggalPeminjaman = rs.getString(6);
                String TanggalPengembalian = rs.getString(7);
                String DendaPelanggaran = rs.getString(8);
                String Status = "";
                if ("K".equals(rs.getString(9)))
                {
                    Status = "Kembali";
                } else
                {
                    Status = "BelumKembali";
                }
                String data[] = {NoAnggota,NamaPeminjam,AlamatPeminjam,JenisBuku,JudulBuku,TanggalPeminjaman,TanggalPengembalian,DendaPelanggaran,Status};
                dtm.addRow(data);
            }
        } catch (SQLException ex)
        {
            Logger.getLogger(frameMain.class.getName()).log(Level.SEVERE, null, ex);
        }
        Tabel.setModel(dtm);
    }
    
