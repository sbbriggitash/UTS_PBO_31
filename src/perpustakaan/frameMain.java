    public void setJam()
    {
        ActionListener taskPerformer = new ActionListener()
        {
            public void actionPerformed(ActionEvent evt)
            {
                Date dt = new Date();
                int nilai_hour = dt.getHours();
                int nilai_minute = dt.getMinutes();
                int nilai_second = dt.getSeconds();
                if (nilai_hour <= 9)
                {
                    zero_hour = "0";
                }
                if (nilai_minute <= 9)
                {
                    zero_minute = "0";
                }
                if (nilai_second <= 9)
                {
                    zero_second = "0";
                }
                String hour = zero_hour + Integer.toString(nilai_hour);
                String minute = zero_minute + Integer.toString(nilai_minute);
                String second = zero_second + Integer.toString(nilai_second);
                Waktu.setText("Pukul " + hour + ":" + minute + ":" + second);
            }
        };
        new Timer(100, taskPerformer).start();
    }
